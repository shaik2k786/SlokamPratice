package com.basic;

public class SwitchCase {
public static void main(String[] args) {
	int days=1;
	switch (days) {
	case 1:
		System.out.println("monday");
		break;
	case 2:
		System.out.println("theusday");
		break;
	case 3:
		System.out.println("wensday");
		break;
	default:
		System.out.println("sunday");
		break;
	}
	
}
}
